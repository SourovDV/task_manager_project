import 'package:task_manager_project/data/models/usermodel/taskdetails.dart';

class ShowListOfTask {
  String? status;
  List<Data>? data;

  ShowListOfTask({this.status, this.data});

  ShowListOfTask.fromJson(Map<String, dynamic> json) {
    status = json['status'];
    if (json['data'] != null) {
      data = <Data>[];
      json['data'].forEach((v) {
        data!.add(new Data.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['status'] = status;
    if (this.data != null) {
      data['data'] = this.data!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

